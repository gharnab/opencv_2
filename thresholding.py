import numpy as np
import cv2

img=cv2.imread('gradient.png',0)
_,th1=cv2.threshold(img,127,255,cv2.THRESH_BINARY)
_,th2=cv2.threshold(img,127,255,cv2.THRESH_BINARY_INV)
_,th3=cv2.threshold(img,100,255,cv2.THRESH_TRUNC)
_,th4=cv2.threshold(img,100,255,cv2.THRESH_TOZERO)
_,th5=cv2.threshold(img,100,255,cv2.THRESH_TRIANGLE)
_,th6=cv2.threshold(img,100,255,cv2.THRESH_MASK)
_,th7=cv2.threshold(img,100,255,cv2.THRESH_OTSU)
_,th8=cv2.threshold(img,100,255,cv2.THRESH_TOZERO_INV)
cv2.imshow("Image",img)
cv2.imshow("thres",th1)
cv2.imshow("inverse",th2)
cv2.imshow("trunc",th3)
cv2.imshow("tozero",th4)
cv2.imshow("triangle",th5)
cv2.imshow("mask",th6)
cv2.imshow("otsu",th7)
cv2.imshow("tozero_reverse",th8)
k=cv2.waitKey(0)
if k==ord('s'):
    cv2.imwrite("threshold.jpg",th1)
    cv2.imwrite("threshold_rev.jpg",th2)
    cv2.imwrite("threshold_trunc.jpg",th3)
    cv2.imwrite("threshold_tozero.jpg",th4)
    cv2.imwrite("triangle.jpg",th5)
    cv2.imwrite("mask.jpg",th6)
    cv2.imwrite("otsu.jpg",th7)
    cv2.imwrite("tozero_reverse.jpg",th8)
cv2.destroyAllWindows()